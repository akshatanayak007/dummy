        public async Task<IActionResult> Details(int? id)
        {
            if (id == null || _context.Book == null)
            {
                return Content("Book Not Found");
            }

            var book = await _context.Book
                .FirstOrDefaultAsync(m => m.Id == id);
            if (book == null)
            {
                return Content("Book Not Found");
            }

            return View(book);
